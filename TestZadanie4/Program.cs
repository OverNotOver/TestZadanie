using Microsoft.EntityFrameworkCore;
using System;
using TestZadanie4.Models.Db;
using TestZadanie4.Services;

namespace TestZadanie4
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();
            builder.Services.AddRazorPages().AddRazorRuntimeCompilation();

            var connect = builder.Configuration.GetConnectionString("ConnectDb");
            builder.Services.AddDbContext<ConnectDb>(opt => opt.UseSqlServer(connect));
            builder.Services.AddScoped<BookServices>();
            builder.Services.AddScoped<AuthorServices>();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
    }
}